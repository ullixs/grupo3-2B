    <div class="recipe-container">
        <h2>Modo de Preparo:</h2>
        <ol>
        <li> Em uma frigideira, coloque um fio de azeite e adicione a carne moída, refogue até secar a água. </li>

<li>Tempere com o sal, a pimenta do reino e o orégano, misture e acrescente o molho de tomate,  misture e deixe cozinhar por 3 minutos.</li>
<li> Reserve ao lado. </li>
<li> Em outra frigideira, coloque a manteiga sem sal e misture até derreter, adicione a farinha de trigo e cozinhe por 2 minutos. </li>
<li> Após esse tempo, adicione ao leite aos poucos e vá misturando. </li>
<li> Tempere com o sal, a pimenta do reino e a noz moscada, misture. </li>
<li> Agora, para montar a receita, pegue um refratário médio e adicione duas conchas do molho branco que preparamos e espalhe. </li>
<li> Acrescente 2 pacotes de miojo um ao lado do outro, assim como no vídeo. </li>
<li> Por cima do miojo, coloque metade da carne moída refogada, 150g de queijo mussarela ralado, cubra com molho branco. </li>
<li> Adicione +2 pacotes do miojo, adicione o restante da carne moída, +150g de queijo ralado e espalhe. </li>
<li> Leve ao forno pré-aquecido a 180 graus e deixe assar por 30 minutos. </li>
<li> Sirva-se.</li>
        </ol>
    </div>
