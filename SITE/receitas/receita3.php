    <!-- Coluna de Modo de Preparo -->
    <div class="recipe-container">
        <h2>Modo de Preparo:</h2>
        <ol>
       <li> Em uma tigela, coloque o camarão pré-cozido e já limpos. </li>

<li> Tempere-os com o sal, a pimenta do reino, o suco de limão, misture bem e deixe descansar por 15 minutos.</li>

<li> Corte a cebola em cubinhos e reserve. </li> 

<li> Corte o tomate ao meio, retire todas as sementes e corte em cubinhos. </li>

<li> Agora, em uma frigideira, coloque um fio de azeite, adicione os camarões e frite até dourar. </li>

<li> Apague o fogo e reserve. </li>

<li> Depois, em outra frigideira, coloque um fio de azeite, a cebola picada em cubinhos, frite até dourar. </li>

<li> Adicione o molho de tomate, o creme de leite e o requeijão cremoso, misture tudo muito bem. </li>

<li> Tempere com o sal, a pimenta do reino e misture. </li>

<li> Adicione o camarão que fritamos, o tomate picado e cheiro verde a gosto, misture e deixe cozinhar por +2 minutos. </li>
 
<li> Sirva-se com arroz e batata palha. </li>
        </ol>
    </div>
