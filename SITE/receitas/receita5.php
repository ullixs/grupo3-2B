
<div class="col-md-6">

    <div class="recipe-container">
        <h2>Ingredientes:</h2>
        <ul>
       <li> 200g de Avelã Descascadas </li>
<li> 2 Colheres (Sopa) de Óleo de Coco </li>
<li> ½ Xícara (Chá) de Cacau em Pó (Xícara de 240 ml) </li>
<li> 1 Colher (Sopa) de Essência de Baunilha </li>
<li> ⅓ Xícara (Chá) de Ado</li>ante em Pó Xilitol </li>
        </ul>

    </div>
</div>

<div class="col-md-6">

    <div class="recipe-container">
        <h2>Modo de Preparo:</h2>
        <ol>
      <li>  Comece colocando as avelãs em uma assadeira e espalhe bem. </li>

<li> Leve para o forno pré-aquecido a 200 graus e deixe assar por 10 minutos. </li>

<li> Após esse tempo, passe as avelãs para um liquidificador ou processador, adicione o óleo de coco e bata até triturar bem. </li>

<li> Depois adicione o cacau em pó, a essência de baunilha e o adoçante xilitol, bata novamente até homogeneizar. </li>

<li> Passe tudo para um pote com tampa. </li>

<li> Sirva-se. </li>
        </ol>
    </div>
</div>