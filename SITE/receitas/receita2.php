<li> 3 Colheres (Sopa) de Manteiga sem Sal </li>
<li> +1 Colher (Sopa) de Manteiga sem Sal </li>
<li> 3 Dentes de Alho </li>
<li> 500ml de Creme de Leite Fresco </li>
<li> 50g de Queijo Parmesão Ralado </li>
<li> 1 Pitada de Noz Moscada </li>
<li> Salsinha a Gosto </li>
        </ul>
        <h4>Para Empanar</h4>
        <ul>
<li> ½ Xícara (Chá) de Farinha de Trigo (Xícara de 240ml) </li>
<li> 50g de Queijo Parmesão Ralado </li>
<li> ½ Colher (Chá) de Sal </li>
        </ul>
    </div>
