<div class="col-md-6">
    <!-- Coluna de Modo de Preparo -->
    <div class="recipe-container">
        <h2>Modo de Preparo:</h2>
        <ol>
<li> Em uma tigela, coloque os peitos de frango, o sal e a pimenta do reino, misture bem e reserve. </li>

<li> Em outra tigela, coloque a farinha de trigo, o queijo parmesão e o sal, misture e empane os peitos de frango. </li>

<li> Em uma frigideira, coloque um fio de azeite, a manteiga sem sal e misture até derreter. </li>

<li> Feito isso, frite os peitos de frango até dourar os dois lados. </li>

<li> Na mesma frigideira, coloque +1 colher de manteiga sem sal e misture até derreter. </li>

<li> Adicione os dentes de alho fatiados e misture até dourar. </li> 

<li> Acrescente o creme de leite fresco e misture por 3 minutos. </li>

<li> Agora, adicione uma pitada de pimenta do reino, o queijo parmesão ralado, uma pitada de sal, uma pitada de noz moscada, misture até engrossar. </li>

<li> Feito isso, corte os peitos de frango em tiras e acrescente-os no molho. </li>

<li> Finalize com salsinha, a gosto. </li>

<li> Sirva-se. </li>
        </ol>
    </div>
</div>