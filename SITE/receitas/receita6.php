<div class="col-md-6">
    <!-- Coluna de Modo de Preparo -->
    <div class="recipe-container">
        <h2>Modo de Preparo:</h2>
        <ol>
   <li>     Comece cortando uma tira de cada batata já descascada, e retire todo o miolo, assim como no vídeo. </li>

<li> Passe as batatas para uma assadeira antiaderente e reserve. </li>

<li> Em uma tigela, coloque o extrato de tomate e o óleo, misture e adicione o molho nas batatas, espalhando bem por dentro de cada uma. </li>

<li> Adicione uma pitada de sal. </li>

<li> Leve-as para o forno pré-aquecido a 200 graus e deixe assar por 40 minutos. </li>

<li> Enquanto isso, em uma frigideira, coloque um fio de azeite, a cebola picada em cubinhos e refogue até dourar. </li>

<li> Adicione a carne moída e refogue até secar toda água. </li>

<li> Tempere com o sal, a páprica doce, a pimenta do reino, adicione os dentes de alho triturados e refogue por 2 minutos. </li>

<li> Adicione salsinha a gosto, misture e reserve. </li>

<li> Agora, em uma tigela, coloque o extrato de tomate e a água, misture bem. </li>

<li> Após a batata ter assado, recheie cada uma com a carne moída que preparamos. </li>
 
<li> Por cima de cada uma, coloque 25g de queijo ralado. </li>

<li> Despeje o molho de tomate na assadeira. </li> 

<li> Leve ao forno pré-aquecido a 200 graus e deixe assar por 15 minutos. </li>

<li> Sirva-se. </li>
        </ol>
    </div>
</div>